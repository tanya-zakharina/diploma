<h2>1. Что такое файлы cookie?</h2>
<p>Файлы cookie — это небольшие текстовые файлы, которые загружаются на ваш компьютер или мобильное устройство при посещении веб-сайта. Они позволяют сайту распознавать ваше устройство и сохранять информацию о ваших предпочтениях или прошлых действиях.</p>

<h2>2. Как мы используем файлы cookie?</h2>
<ul>
    <li><span class="highlight">Строго необходимые:</span> Нужны для обеспечения базовых функций, таких как безопасность, управление сетью и доступность. Без них сайт не сможет работать корректно.</li>
    <li><span class="highlight">Функциональные:</span> Используются для запоминания выбранных вами настроек (например, язык, регион или данные формы обратной связи).</li>
    <li><span class="highlight">Аналитические:</span> Помогают нам понять, как посетители взаимодействуют с сайтом, собирая анонимную информацию. Мы используем их для улучшения структуры и контента.</li>
    <li><span class="highlight">Маркетинговые:</span> Используются для отслеживания эффективности рекламы и предоставления более релевантных объявлений.</li>
</ul>

